'''
Python Casting

There may be times when you want to specify a type on to a variable.
This can be done with casting.
Python is an object-orientated language, and as such it uses classes to define data types,
including its primitive types.

'''

# Integers:

x = int(122)
y = int(2.822)
z = int("31211")
print(x)
print(y)
print(z)
print(type(x))
print(type(y))
print(type(z))



#  Floats:

x = float(1)
y = float(2.8)
z = float("3")
w = float("4.2")
print(x)
print(y)
print(z)
print(w)
print(type(x))
print(type(y))
print(type(z))
print(type(w))

#  Strings:


x = str("s122")
y = str(2)
z = str(3.0)
print(x)
print(y)
print(z)
print(type(x))
print(type(y))
print(type(z))